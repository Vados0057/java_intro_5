package casting;

public class Min_Max_Of_Primitives {
    public static void main(String[] args) {
        System.out.println("The min value of the byte" + Byte.MIN_VALUE);
        System.out.println("The max value of the byte" + Byte.MAX_VALUE);

        System.out.println("The min value of the int" + Integer.MIN_VALUE);
        System.out.println("The max value of the int" + Integer.MAX_VALUE);

        System.out.println("The max value of the short" + Short.MAX_VALUE);
        System.out.println("The max value of the short" + Short.MAX_VALUE);


        System.out.println("The max value of the short" + Character.MAX_VALUE);
        System.out.println("The max value of the short" + Character.MAX_VALUE);



    }
}
