package practice.variablesPractice;

public class BooleanAndCharPractice {
    public static void main(String[] args){
        boolean negative = false;
        boolean positive = true;

        System.out.println("negative = " + "\"" + negative + "\"");
        System.out.println("positive = " + "\"" + positive + "\"");

        char myFirstChar = 'V';
        char mySecondChar = 'l';
        char myThirdChar = 'a';
        char myFourthChar = 'd';

        System.out.print(myFirstChar);
        System.out.print(mySecondChar);
        System.out.print(myThirdChar);
        System.out.print(myFourthChar + "\n" + "\n");
        System.out.println(myFirstChar + "" + mySecondChar + "" + myThirdChar + "" + myFourthChar);

        char firstLetter = 'C';
        char secondLatter = 'a';
        char thirdLatter = 't';

        System.out.print("\"" + firstLetter);
        System.out.print(secondLatter);
        System.out.print(thirdLatter + "\"");
        System.out.println();

        char letter1 = 'D';
        char letter2 = 'o';
        char letter3 = 'g';
        System.out.print("\"" +letter1);
        System.out.print(letter2);
        System.out.print(letter3 + "\"");



    }
}
