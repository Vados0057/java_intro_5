package print_statements;

public class PrintVsPrintln {
    public static void main(String[] args){


        // println
        // println print a given text and move cursor to the next line
        System.out.println("This is println");
        System.out.println("Vladyslav Ksendziuk");
        System.out.println("Today is Sunday");


        //print
        //print print a given text right next to previous one
        System.out.print("This is println");
        System.out.print("Vladyslav Ksendziuk");
        System.out.print("Today is Sunday");
    }
}
